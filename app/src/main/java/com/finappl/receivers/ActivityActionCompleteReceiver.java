package com.finappl.receivers;

import android.content.BroadcastReceiver;
import android.content.Context;
import android.content.Intent;

import com.finappl.services.NotificationsService;

/**
 * Created by ajit on 25/9/15.
 */
public class ActivityActionCompleteReceiver extends BroadcastReceiver {

    @Override
    public void onReceive(Context context, Intent intent) {

        Intent service = new Intent(context, NotificationsService.class);
        context.startService(service);

    }

}
